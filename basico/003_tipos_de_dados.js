
// String
let nome = "Douglas";
console.log('String: ', typeof nome);

// Number 
let idade = 30
let temperatura = 36.5;
console.log("Inteiro: ", typeof idade, "Float: ",typeof temperatura)

// Boolean
let ativo = true;
console.log("Bollean", typeof ativo); // boolean

// valor nao definido (undefind)
let valor;
console.log("Underfind: ",typeof valor);

// Valor Null
let nada = null;
console.log("Nullo: ", typeof nada) // Retorna OBJECT erro do JS

// Symbol (Indentificadores Únicos)
let id = Symbol("id");
console.log("Indentificador", typeof id);

// BigInt (Números muito grandes)
let numeroGrande = 123456789012345678901234567890n;
console.log("Numero Grande", typeof numeroGrande);

